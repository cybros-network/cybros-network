/// The type used to represent the kinds of proxying allowed.
#[derive(Copy, Clone, Eq, PartialEq, Ord, PartialOrd, Encode, Decode, RuntimeDebug, MaxEncodedLen, TypeInfo)]
pub enum ProxyType {
	Any,
	NonTransfer,
}
impl Default for ProxyType {
	fn default() -> Self {
		Self::Any
	}
}
impl InstanceFilter<RuntimeCall> for ProxyType {
	fn filter(&self, c: &RuntimeCall) -> bool {
		match self {
			ProxyType::Any => true,
			ProxyType::NonTransfer => !matches!(
				c,
				RuntimeCall::Balances(..) | RuntimeCall::Vesting(pallet_vesting::Call::vested_transfer { .. })
			),
		}
	}
	fn is_superset(&self, o: &Self) -> bool {
		match (self, o) {
			(x, y) if x == y => true,
			(ProxyType::Any, _) => true,
			(_, ProxyType::Any) => false,
			(ProxyType::NonTransfer, _) => true,
			// _ => false,
		}
	}
}

// Create the runtime by composing the FRAME pallet_configs that were previously configured.
construct_runtime!(
	pub struct Runtime
	where
		Block = Block,
		NodeBlock = opaque::Block,
		UncheckedExtrinsic = UncheckedExtrinsic,
	{
		// System support
		System: frame_system = 0,
		Timestamp: pallet_timestamp = 1,
		RandomnessCollectiveFlip: pallet_insecure_randomness_collective_flip = 2,

		// Consensus
		Aura: pallet_aura = 20,
		Grandpa: pallet_grandpa = 21,

		// Utilities
		Utility: pallet_utility = 40,
		Multisig: pallet_multisig = 41,
		Proxy: pallet_proxy = 42,

		// Monetary
		Balances: pallet_balances = 60,
		TransactionPayment: pallet_transaction_payment = 61,
		Vesting: pallet_vesting = 62,

		// The main stage
		OffchainComputingWorkers: pallet_offchain_computing_workers = 100,
		OffchainComputing: pallet_offchain_computing = 101,

		// Non-permanent
		Sudo: pallet_sudo = 255,
	}
);

